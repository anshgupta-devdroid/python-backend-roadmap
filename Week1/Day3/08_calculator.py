first_number = float(input("Enter the first number: "))
second_number = float(input("Enter the second number: "))
sum_result = first_number + second_number
difference_number = first_number - second_number
product_number = first_number * second_number
quotient_number = first_number / second_number
floor_division_number = first_number // second_number
modulus_number = first_number % second_number
power_number = first_number ** second_number
print("====== Simple Calculator ======")
print(f"Sum: {sum_result}")
print(f"Difference: {difference_number}")
print(f"Product: {product_number}")
print(f"Quotient: {quotient_number:.2f}")
print(f"Floor Division: {floor_division_number}")
print(f"Modulus: {modulus_number}")
print(f"Power: {power_number}")
print("================================")